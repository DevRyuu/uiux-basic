/**
 * Variable 선언하기
 */

 var name  = '코드팩토리';
 console.log(name);

 var age = 36;
 console.log(age);

 let ive = '아이브';
 console.log(ive);

 /**
  * let과 var로 선언하면
  * 값을 추후 변경할 수 있다.
  * 
  * let은 더 이상 사용하지 않는다.
  */

ive = '안유진';
console.log(ive);

const newJeans = '뉴진스';
console.log(newJeans);

/**
 * 선언과 할당
 * 1) 변수를 선언하는 것
 * 2) 변수에 값을 할당하는 것
 */

var name;
name = '코드팩토리';
console.log(name);
